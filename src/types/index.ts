/**
 * Legacy activity shape used by ItineraryView and the
 * generate-itinerary route handler.
 */
export interface LegacyActivity {
  id: string;
  time: string;
  title: string;
  description: string;
  location: string;
  duration: string;
  cost: string;
  category:
    | 'sightseeing'
    | 'food'
    | 'adventure'
    | 'culture'
    | 'relaxation'
    | 'transport'
    | 'shopping';
  tips?: string;
}

/**
 * Legacy day plan used by ItineraryView and the
 * generate-itinerary route handler.
 */
export interface DayPlan {
  day: number;
  date: string;
  theme: string;
  activities: LegacyActivity[];
}

// ────────────────────────────────────────────────────────────
// Itinerary (Unified — new fields + legacy fields)
// ────────────────────────────────────────────────────────────

/**
 * The top-level itinerary returned by the AI pipeline.
 *
 * `days` is typed as `DayPlan[]` to match every existing
 * component and API route handler.  For new code that uses
 * the structured `Day` / `TimeBlock` format, see `AIItinerary`.
 */
export interface Itinerary {
  // ── Shared ──────────────────────────────────────────────
  /** Unique identifier (UUID v4) */
  id: string;
  /** Primary destination */
  destination: string;
  /** Optional ISO start date */
  startDate?: string;
  /** ISO timestamp of creation */
  createdAt: string;

  // ── New fields ──────────────────────────────────────────
  /** User-facing trip name (new) */
  name?: string;
  /** Trip length in days (new) */
  duration?: number;
  /** Travel style label (new) */
  travelStyle?: string;
  /** Budget tier label (new) */
  budgetLevel?: string;
  /** Grand total estimated cost (new, numeric) */
  totalEstimatedCost?: number | string;
  /** AI-generated packing suggestions (new) */
  packingTips?: string[];
  /** AI-generated general travel tips (new) */
  generalTips?: string[];
  /** ISO timestamp of last modification (new) */
  updatedAt?: string;

  // ── Legacy fields ───────────────────────────────────────
  /** Short title generated by the AI */
  title?: string;
  /** AI-generated summary paragraph */
  summary?: string;
  /** Optional ISO end date */
  endDate?: string;
  /** Number of calendar days (legacy) */
  totalDays?: number;
  /** Budget tier string ("budget" | "moderate" | "luxury") */
  budget?: string;
  /** Currency code (e.g. "USD") */
  currency?: string;
  /** Day-by-day breakdown — legacy flat format */
  days: DayPlan[];
  /** Flat packing list (legacy) */
  packingList?: string[];
  /** Flat travel tips list (legacy) */
  travelTips?: string[];
  /** Estimated total cost as display string (legacy) */
  estimatedTotalCost?: string;
}

/**
 * New structured itinerary using `Day` / `TimeBlock` / `Activity`.
 * Use this when building new features; migrate existing components
 * to this shape over time.
 */
export interface AIItinerary {
  id: string;
  name: string;
  destination: string;
  duration: number;
  startDate?: string;
  travelStyle: string;
  budgetLevel: string;
  days: Day[];
  totalEstimatedCost: number;
  packingTips?: string[];
  generalTips?: string[];
  createdAt: string;
  updatedAt: string;
}

// ────────────────────────────────────────────────────────────
// User Input
// ────────────────────────────────────────────────────────────

/**
 * The shape of data collected from the trip form before
 * being sent to the AI generation endpoint.
 */
export interface UserInput {
  /** Where the user wants to go */
  destination: string;
  /** How many days (defaults to AI suggestion if omitted) */
  duration?: number;
  /** Tags like "history", "food", "nightlife" */
  interests: string[];
  /** Free-text or structured budget preference */
  budget?: string;
  /** E.g. "Adventure", "Luxury", "Budget" */
  travelStyle?: string;
  /** Accessibility or dietary constraints */
  constraints?: string[];
  /** Optional ISO start date */
  startDate?: string;
  /** Number of travelers */
  groupSize?: number;
}

