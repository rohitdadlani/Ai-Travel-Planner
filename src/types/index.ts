// API Request / Response Contracts
// ────────────────────────────────────────────────────────────

/**
 * Body sent to POST /api/generate-itinerary
 */
export interface GenerateItineraryRequest {
  preferences: TripPreferences;
}

/**
 * Response from POST /api/generate-itinerary
 */
export interface GenerateItineraryResponse {
  success: boolean;
  itinerary?: Itinerary;
  error?: string;
}

/**
 * Body sent to POST /api/enhance-content
 */
export interface EnhanceContentRequest {
  content: string;
  type: 'description' | 'tips' | 'summary';
}

/**
 * Response from POST /api/enhance-content
 */
export interface EnhanceContentResponse {
  success: boolean;
  enhanced?: string;
  error?: string;
}

