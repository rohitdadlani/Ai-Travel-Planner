// ────────────────────────────────────────────────────────────
// Core Data Models (New)
// ────────────────────────────────────────────────────────────

/**
 * A single activity within a time block.
 * Represents one thing the traveler will do (visit a museum,
 * eat at a restaurant, take a bus, etc.).
 */
export interface Activity {
  /** Display name of the activity */
  name: string;
  /** Short description generated by the primary AI */
  description: string;
  /** Richer description optionally enhanced by HuggingFace */
  enhancedDescription?: string;
  /** Human-readable duration (e.g. "2 hours") */
  duration: string;
  /** Estimated cost in the local currency */
  cost: number;
  /** Practical tips for the traveler */
  tips?: string;
  /** Whether advance booking is needed */
  bookingRequired: boolean;
  /** Direct URL to book, if applicable */
  bookingUrl?: string;
  /** Flag for activities that are especially photogenic */
  photoWorthy?: boolean;
  /** Physical address or location name */
  address?: string;
  /** High-level category for icon/color coding in the UI */
  category?: 'Food' | 'Attraction' | 'Transport' | 'Entertainment';
}

/**
 * A chunk of a day grouped by period (Morning, Lunch, etc.).
 */
export interface TimeBlock {
  /** Which part of the day this block covers */
  period: 'Morning' | 'Lunch' | 'Afternoon' | 'Dinner' | 'Evening';
  /** Human-readable time range (e.g. "9:00 AM – 12:00 PM") */
  timeRange: string;
  /** Ordered list of activities within this block */
  activities: Activity[];
}

/**
 * A single day inside an itinerary (new structured format).
 */
export interface Day {
  /** 1-indexed day number */
  dayNumber: number;
  /** Optional ISO date string for the specific calendar day */
  date?: string;
  /** Thematic label (e.g. "Cultural Exploration") */
  theme: string;
  /** Ordered time blocks that make up this day */
  timeBlocks: TimeBlock[];
  /** Sum of all activity costs for the day */
  dayTotal: number;
  /** Transport / logistics notes */
  logistics?: string;
  /** Weather summary, optionally enriched by HuggingFace */
  weatherForecast?: string;
}

// ────────────────────────────────────────────────────────────
// Legacy Data Models (Backward Compatibility)
// ────────────────────────────────────────────────────────────
// These interfaces mirror the original shapes used by the
// existing UI components and API route handlers.  They are
// exported so that current code keeps working without changes.
