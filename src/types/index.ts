/**
 * Preferences shape consumed by the trip form, the prompt
 * builder, and the local-storage persistence layer.
 */
export interface TripPreferences {
  destination: string;
  startDate: string;
  endDate: string;
  budget: 'budget' | 'moderate' | 'luxury';
  interests: string[];
  travelStyle: 'relaxed' | 'balanced' | 'packed';
  travelers: number;
  specialRequirements?: string;
}

